const dictionaries: Record<Locale, Dict> = { en, fa }

type I18nContextValue = {
  locale: Locale
  dir: "ltr" | "rtl"
  setLocale: (l: Locale) => void
  t: (key: keyof typeof en | string) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en")

  useEffect(() => {
    const saved = (typeof window !== "undefined" && localStorage.getItem("locale")) as Locale | null
    if (saved && (saved === "en" || saved === "fa")) {
      setLocaleState(saved)
    }
  }, [])

  useEffect(() => {
    const dir = locale === "fa" ? "rtl" : "ltr"
    document.documentElement.setAttribute("dir", dir)
    document.documentElement.setAttribute("lang", locale)
  }, [locale])

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l)
    if (typeof window !== "undefined") localStorage.setItem("locale", l)
  }, [])

  const t = useCallback(
    (key: string) => dictionaries[locale][key] ?? dictionaries.en[key] ?? key,
    [locale],
  )

  const dir = locale === "fa" ? "rtl" : "ltr"

  return <I18nContext.Provider value={{ locale, dir, setLocale, t }}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider")
  return ctx
}
